function hashString(value) {
  let hash = 0;
  for (let i = 0; i < value.length; i++) {
    hash = hash * 31 + value.charCodeAt(i) >>> 0;
  }
  return hash;
}
function generateFunnyClue(word, seed2) {
  const upper = word.toUpperCase();
  const hints = CLUE_HINTS[upper];
  if (hints && hints.length > 0) {
    const hint = hints[seed2 % hints.length];
    return `${hint.charAt(0).toUpperCase()}${hint.slice(1)} (${upper.length})`;
  }
  const template = FUNNY_CLUE_TEMPLATES[seed2 % FUNNY_CLUE_TEMPLATES.length];
  return template.replace("{len}", String(upper.length));
}
