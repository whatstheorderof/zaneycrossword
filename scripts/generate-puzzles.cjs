function parseTemplate(template) {
  const lines = template.trim().split("\n");
  const rows = lines.length;
  const cols = lines[0].length;
  const blocks = lines.map(
    (line) => line.split("").map((ch) => ch === "#" ? "#" : ".")
  );
  const slots = [];
  for (let r = 0; r < rows; r++) {
    let c = 0;
    while (c < cols) {
      if (blocks[r][c] === "#") {
        c++;
        continue;
      }
      const cells = [];
      while (c < cols && blocks[r][c] !== "#") {
        cells.push([r, c]);
        c++;
      }
      if (cells.length >= 2) {
        slots.push({ direction: "across", cells });
      }
    }
  }
  for (let c = 0; c < cols; c++) {
    let r = 0;
    while (r < rows) {
      if (blocks[r][c] === "#") {
        r++;
        continue;
      }
      const cells = [];
      while (r < rows && blocks[r][c] !== "#") {
        cells.push([r, c]);
        r++;
      }
      if (cells.length >= 2) {
        slots.push({ direction: "down", cells });
      }
    }
  }
  return { rows, cols, blocks, slots };
}
function buildEntriesFromAssignment(slots, assignment, seed2) {
  return slots.map((slot, i) => {
    const answer = assignment[i].toUpperCase();
    const [startRow, startCol] = slot.cells[0];
    return {
      answer,
      clue: generateFunnyClue(answer, seed2 + i * 17 + hashString(answer)),
      direction: slot.direction,
      start: [startRow, startCol]
    };
  });
}

